    private int tentativas = 0;

    private void jBtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEntrarActionPerformed
        // TODO add your handling code here:
        String username = jTxtJMJ_Apelido.getText();
        String senha = new String(jPwfJMJ_Senha.getPassword());
        
        DAO_JmjUsuarios usuariosDao = new DAO_JmjUsuarios();
        JmjUsuarios usuarios = (JmjUsuarios) usuariosDao.listLogin(username, senha);

        if(username.equals("") || senha.equals("")){
            JOptionPane.showMessageDialog(this, "Usuário e/ou senha em branco. Preencha corretamente!");
        } else if (usuarios != null && username.equals(usuarios.getJmjApelido()) && senha.equals(usuarios.getJmjSenha())) {
            this.dispose();
            JFrmPrincipal principal = new JFrmPrincipal();
            principal.setVisible(true);
        } else {
            tentativas++;

            if (tentativas >= 3) {
                JOptionPane.showMessageDialog(this, "Número máximo de tentativas atingido. O sistema será fechado.");
                System.exit(0);
            } else {
                JOptionPane.showMessageDialog(this, "Usuário ou senha incorretos. Tentativas restantes: " + (3 - tentativas));
            }
        }
    }//GEN-LAST:event_jBtnEntrarActionPerformed

    
