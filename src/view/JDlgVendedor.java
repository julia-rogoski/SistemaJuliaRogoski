    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
         // TODO add your handling code here:
        // habilitar();
        int resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão ?");
        if (resp == JOptionPane.YES_OPTION) {
            JMJ_Vendedor vendedor = new JMJ_Vendedor();
             int codigo = Integer.parseInt(jTxtCodigo.getText());
             vendedor.setjmj_idVendedor(codigo);
             vendedor.setjmj_Nome( jTxtNome.getText() );
             vendedor.setjmj_cpf(jFmtCpf.getText());
             vendedor.setjmj_Email(jTxtEmail.getText());
             vendedor.setjmj_dataNascimento(null);
             vendedor.setjmj_telefone(jTxtTelefone.getText());
             vendedor.setjmj_celular(jTxtCelular.getText());
             vendedor.setjmj_cep(jFmtCep.getText());
             vendedor.setjmj_endereco(jTxtEndereco.getText());
             vendedor.setjmj_bairro(jTxtBairro.getText());
             vendedor.setjmj_cidade(jTxtCidade.getText());
             vendedor.setjmj_uf(jTxtUf.getText());
             vendedor.setjmj_dataEmissao(null);
             if (jCboAtivo.isSelected() == true) {
                vendedor.setjmj_Ativo("S");
             } else {
                vendedor.setjmj_Ativo("N");
             }

            JMJ_VendedorDao vendedorDao = new JMJ_VendedorDao();
            vendedorDao.delete(vendedor);
        }
        limpar();
    }//GEN-LAST:event_jBtnExcluirActionPerformed

