    private void jBtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPesquisarActionPerformed
        // TODO add your handling code here:
       // habilitar();
        String id = JOptionPane.showInputDialog(null, "Entre com o código ?");
        int codigo = Integer.valueOf(id);
        JMJ_VendedorDao vendedorDao = new JMJ_VendedorDao();
        JMJ_Vendedor vendedor = (JMJ_Vendedor) vendedorDao.list(codigo);
        if (vendedor == null) {
            JOptionPane.showMessageDialog(null, "Código não existe!");
        } else {
            jTxtCodigo.setText(id);
            jTxtNome.setText(vendedor.getjmj_Nome());
            jFmtCpf.setText(vendedor.getjmj_Cpf());
            jTxtEmail.setText(vendedor.getjmj_Cpf());
            //jFmtDataNascimento.setText(usuarios.getDataNascimento());
            jFmtDataNascimento.setText(vendedor.getjmj_Cpf());
            jTxtTelefone.setText(vendedor.getjmj_Cpf());
            jTxtCelular.setText(vendedor.getjmj_Cpf());
            jFmtCep.setText(vendedor.getjmj_Cpf());
            jTxtEndereco.setText(vendedor.getjmj_Cpf());
            jTxtBairro.setText(vendedor.getjmj_Cpf());
            jTxtCidade.setText(vendedor.getjmj_Cpf());
            jTxtUf.setText(vendedor.getjmj_Cpf());
            jFmtDataEmissao.setText(vendedor.getjmj_Cpf());
            if (vendedor.getjmj_Ativo().equals("S")) {
                jCboAtivo.setSelected(true);
            } else {
                jCboAtivo.setSelected(false);
            }
        }
    }//GEN-LAST:event_jBtnPesquisarActionPerformed

